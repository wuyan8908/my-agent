#!/usr/bin/env node

const fs = require("fs/promises");
const path = require("path");
const {
  INSTALL_REPORT_PATH,
  MANIFEST_PATH,
  isManagedPath,
  isMetadataPath,
  toPortablePath
} = require("./lib/install-paths");

async function mergeIntoRepo(targetDir, filePlan, options = {}) {
  const mode = options.mode || "safe";
  const dryRun = Boolean(options.dryRun);
  const previousState = await loadInstallState(targetDir);
  const ownedPaths = new Set(previousState.toolOwnedFiles.map((entry) => entry.path));
  const plannedPaths = new Set(filePlan.map((item) => toPortablePath(item.targetPath)));
  const results = [];

  for (const item of filePlan) {
    const relativePath = toPortablePath(item.targetPath);
    const absolutePath = path.join(targetDir, relativePath);
    const exists = await pathExists(absolutePath);
    const ownership = await classifyOwnership(absolutePath, relativePath, exists, ownedPaths);
    const decision = decideAction({ exists, ownership, mode, relativePath });

    results.push({
      path: relativePath,
      sourceTemplate: item.sourceTemplate || null,
      ownership,
      action: decision
    });

    if (decision === "skipped-tool-owned" || decision === "skipped-user-owned" || decision === "manual-review") {
      continue;
    }

    if (!dryRun) {
      await fs.mkdir(path.dirname(absolutePath), { recursive: true });
      await fs.writeFile(absolutePath, item.content, "utf8");
    }
  }

  if (mode === "replace") {
    for (const previousEntry of previousState.toolOwnedFiles) {
      if (isMetadataPath(previousEntry.path) || plannedPaths.has(previousEntry.path)) continue;
      if (!isManagedPath(previousEntry.path)) continue;

      const absolutePath = path.join(targetDir, previousEntry.path);
      const exists = await pathExists(absolutePath);
      if (!exists) continue;

      results.push({
        path: previousEntry.path,
        sourceTemplate: previousEntry.sourceTemplate || null,
        ownership: "tool-owned",
        action: dryRun ? "planned-delete" : "deleted"
      });

      if (!dryRun) {
        await fs.rm(absolutePath, { force: true });
      }
    }
  }

  const summary = summarizeResults(results);

  return {
    mode,
    dryRun,
    previousState,
    summary,
    results
  };
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function readJson(filePath) {
  try {
    const raw = await fs.readFile(filePath, "utf8");
    return JSON.parse(raw);
  } catch {
    return null;
  }
}

async function loadInstallState(targetDir) {
  const manifest = await readJson(path.join(targetDir, MANIFEST_PATH));
  const installReport = await readJson(path.join(targetDir, INSTALL_REPORT_PATH));
  const toolOwnedFiles = new Map();

  for (const source of [manifest, installReport]) {
    for (const entry of source?.toolOwnedFiles || []) {
      toolOwnedFiles.set(toPortablePath(entry.path), {
        path: toPortablePath(entry.path),
        sourceTemplate: entry.sourceTemplate || null,
        checksum: entry.checksum || null
      });
    }
  }

  return {
    manifest,
    installReport,
    toolOwnedFiles: Array.from(toolOwnedFiles.values())
  };
}

async function classifyOwnership(absolutePath, relativePath, exists, ownedPaths) {
  if (!exists) return "missing";
  if (ownedPaths.has(relativePath)) return "tool-owned";

  if (isManagedPath(relativePath)) {
    const prefix = await readFilePrefix(absolutePath);
    if (prefix.startsWith("<!-- Generated by Codex Team Bootstrapper")) {
      return "tool-owned";
    }

    return "unknown-managed";
  }

  return "user-owned";
}

async function readFilePrefix(filePath) {
  try {
    const content = await fs.readFile(filePath, "utf8");
    return content.slice(0, 160);
  } catch {
    return "";
  }
}

function decideAction({ exists, ownership, mode, relativePath }) {
  if (!exists) return "created";
  if (!isManagedPath(relativePath)) return "skipped-user-owned";

  if (mode === "safe") {
    if (ownership === "tool-owned") return "skipped-tool-owned";
    if (ownership === "unknown-managed") return "manual-review";
    return "skipped-user-owned";
  }

  if (mode === "smart") {
    if (ownership === "tool-owned") return "updated";
    if (ownership === "unknown-managed") return "manual-review";
    return "skipped-user-owned";
  }

  if (mode === "replace") {
    if (ownership === "tool-owned") return "updated";
    if (ownership === "unknown-managed") return "manual-review";
    return "skipped-user-owned";
  }

  throw new Error(`Unsupported install mode: ${mode}`);
}

function summarizeResults(results) {
  const createdFiles = [];
  const updatedFiles = [];
  const skippedFiles = [];
  const manualReviewFiles = [];
  const deletedFiles = [];

  for (const result of results) {
    if (result.action === "created") createdFiles.push(result.path);
    if (result.action === "updated") updatedFiles.push(result.path);
    if (result.action === "deleted") deletedFiles.push(result.path);
    if (result.action === "planned-delete") deletedFiles.push(result.path);
    if (result.action === "skipped-tool-owned" || result.action === "skipped-user-owned") {
      skippedFiles.push(result.path);
    }
    if (result.action === "manual-review") {
      manualReviewFiles.push(result.path);
    }
  }

  return {
    createdFiles,
    updatedFiles,
    skippedFiles,
    manualReviewFiles,
    deletedFiles,
    conflictsAvoided: manualReviewFiles.length
  };
}

async function readPlan(planArg) {
  if (!planArg || planArg === "-") {
    const chunks = [];
    for await (const chunk of process.stdin) chunks.push(chunk);
    return JSON.parse(Buffer.concat(chunks).toString("utf8"));
  }

  const raw = await fs.readFile(path.resolve(planArg), "utf8");
  return JSON.parse(raw);
}

function parseArgs(argv) {
  const parsed = { _: [] };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (!arg.startsWith("--")) {
      parsed._.push(arg);
      continue;
    }

    const [key, inlineValue] = arg.slice(2).split("=");
    if (inlineValue !== undefined) {
      parsed[key] = inlineValue;
      continue;
    }

    if (key === "dry-run") {
      parsed.dryRun = true;
      continue;
    }

    parsed[key] = argv[index + 1];
    index += 1;
  }

  return parsed;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const targetDir = path.resolve(args._[0] || process.cwd());
  const plan = await readPlan(args.plan);
  const filePlan = Array.isArray(plan) ? plan : plan.filePlan;

  if (!Array.isArray(filePlan)) {
    throw new Error("Plan must be an array or an object with a filePlan array.");
  }

  const result = await mergeIntoRepo(targetDir, filePlan, {
    mode: args.mode || "safe",
    dryRun: args.dryRun
  });

  console.log(JSON.stringify(result, null, 2));
}

if (require.main === module) {
  main().catch((error) => {
    console.error(error.message);
    process.exit(1);
  });
}

module.exports = {
  loadInstallState,
  mergeIntoRepo
};
